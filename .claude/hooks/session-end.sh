#!/usr/bin/env bash
#
# Claude Code SessionEnd Hook - Handover Context Generation
#
# Creates NEXT-SESSION.md with handover notes for continuity
# Runs in background when session ends (Ctrl-C, /exit, logout)
#

set -uo pipefail

# Read hook input
HOOK_INPUT=$(cat 2>/dev/null || echo '{}')
SESSION_ID=$(echo "$HOOK_INPUT" | jq -r '.session_id // "unknown"' 2>/dev/null || echo "unknown")
REASON=$(echo "$HOOK_INPUT" | jq -r '.reason // "unknown"' 2>/dev/null || echo "unknown")
TIMESTAMP=$(date +"%Y-%m-%d %H:%M:%S")

# Log that hook triggered
echo "ðŸª SessionEnd: Generating handover context (session: $SESSION_ID, reason: $REASON)" >&2

# ============================================================================
# Generate NEXT-SESSION.md with handover context
# ============================================================================

HANDOVER_FILE="NEXT-SESSION.md"

cat > "$HANDOVER_FILE" << 'HEADER'
# Next Session Handover

**Auto-generated by SessionEnd hook**

HEADER

echo "**Last Updated**: $TIMESTAMP" >> "$HANDOVER_FILE"
echo "**Session ID**: $SESSION_ID" >> "$HANDOVER_FILE"
echo "" >> "$HANDOVER_FILE"

# Active Work Items
echo "## ðŸ“‹ Active Work Items" >> "$HANDOVER_FILE"
echo "" >> "$HANDOVER_FILE"
if command -v apm &> /dev/null; then
    ACTIVE_WI=$(apm work-item list --status=in_progress --status=review 2>/dev/null | grep -E "^\â”‚" | grep -v "^â”ƒ" | grep -v "^â”" | grep -v "^â”—" | grep -v "^â”¡" | head -5 || echo "")
    if [ -n "$ACTIVE_WI" ]; then
        echo "\`\`\`" >> "$HANDOVER_FILE"
        echo "$ACTIVE_WI" >> "$HANDOVER_FILE"
        echo "\`\`\`" >> "$HANDOVER_FILE"
    else
        echo "No active work items" >> "$HANDOVER_FILE"
    fi
else
    echo "âš ï¸ apm not available" >> "$HANDOVER_FILE"
fi
echo "" >> "$HANDOVER_FILE"

# Active Tasks
echo "## âœ… Active Tasks" >> "$HANDOVER_FILE"
echo "" >> "$HANDOVER_FILE"
if command -v apm &> /dev/null; then
    ACTIVE_TASKS=$(apm task list --status=in_progress --status=review 2>/dev/null | grep -E "^\â”‚" | grep -v "^â”ƒ" | grep -v "^â”" | grep -v "^â”—" | grep -v "^â”¡" | head -5 || echo "")
    if [ -n "$ACTIVE_TASKS" ]; then
        echo "\`\`\`" >> "$HANDOVER_FILE"
        echo "$ACTIVE_TASKS" >> "$HANDOVER_FILE"
        echo "\`\`\`" >> "$HANDOVER_FILE"
    else
        echo "No active tasks" >> "$HANDOVER_FILE"
    fi
fi
echo "" >> "$HANDOVER_FILE"

# Uncommitted Changes
echo "## ðŸ”„ Uncommitted Changes" >> "$HANDOVER_FILE"
echo "" >> "$HANDOVER_FILE"
if git status --short 2>/dev/null | grep -q .; then
    echo "\`\`\`" >> "$HANDOVER_FILE"
    git status --short 2>/dev/null | head -20 >> "$HANDOVER_FILE"
    echo "\`\`\`" >> "$HANDOVER_FILE"
    echo "" >> "$HANDOVER_FILE"
    echo "âš ï¸ **Action needed**: Review and commit changes" >> "$HANDOVER_FILE"
else
    echo "âœ… Working tree is clean" >> "$HANDOVER_FILE"
fi
echo "" >> "$HANDOVER_FILE"

# Recent Commits
echo "## ðŸ“ Recent Commits (Last 3)" >> "$HANDOVER_FILE"
echo "" >> "$HANDOVER_FILE"
if git log --oneline -3 2>/dev/null; then
    echo "\`\`\`" >> "$HANDOVER_FILE"
    git log --oneline -3 2>/dev/null >> "$HANDOVER_FILE"
    echo "\`\`\`" >> "$HANDOVER_FILE"
fi
echo "" >> "$HANDOVER_FILE"

# Quick Start Commands
cat >> "$HANDOVER_FILE" << 'COMMANDS'
## ðŸš€ Quick Start Commands

```bash
# Check status
apm status

# View active work
apm work-item list --status=in_progress
